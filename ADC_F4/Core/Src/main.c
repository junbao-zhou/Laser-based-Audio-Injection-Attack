void transmit_16_t(UART_HandleTypeDef *huart, uint16_t num) {
	static uint8_t msg[2];
	msg[0] = num & 0xFF;
	msg[1] = (num & 0xFF00) >> 8;
	HAL_UART_Transmit(huart, msg, 2, HAL_MAX_DELAY);
}
uint16_t rescale(uint16_t num, uint16_t min, uint16_t max) {
	static const uint16_t target_min = 0;
	static const uint16_t target_max = 0xFFF;
	if (num <= min)
		return target_min;
	else if (num >= max)
		return target_max;
	else
		return (double) (num - min) / (double) (max - min)
				* (double) (target_max - target_min) + target_min;
}
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	HAL_UART_Transmit(huart, &aRxBuffer1, 1, HAL_MAX_DELAY);
	if (aRxBuffer1 == 'm') {
		min = 0xFFFF;
		max = 0;
	}
	else if (aRxBuffer1 == 'r')
	{
		r_min = min;
		r_max = max;
	}
	HAL_UART_Receive_IT(huart, &aRxBuffer1, 1); // save char from uart receive
}
